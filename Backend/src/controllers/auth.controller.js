const userModel =require("../models/user.model")
const bcrypt = require("bcryptjs");
const jwt = require("jsonwebtoken")
const tokenBlacklistModel = require("../models/blacklist.model")

async function registerUserController(req,res){
    const {username,email,password} = req.body

    console.log("REGISTER BODY:", req.body);

    if(!username || !email || !password) {
        return res.status(400).json({
            message : "all feilds are required"
        })
    }

    //find the user with the given email or username
    const isUserExist = await userModel.findOne({
        $or : [
            {username},
            {email}
        ]
    })

    if(isUserExist) {
        return res.status(400).json({
            message : "account already exist with this email or username"
        })
    }
    //hash the password before saving to db why because we dont want to save the password in plain text in db for security done but how hashing works ? hashing is a one way function which takes the input and gives the output but we cannot get the input from the output so if we hash the password and save it in db then even if someone get access to our db they cannot get the password in plain text form because they cannot reverse the hash to get the original password so hashing is a very good way to secure our password in db
    const hash = await bcrypt.hash(password,10)

    //created the user in db
    const user = await userModel.create({
        username,
        email,
        password : hash
    })
    //what exactly token is ? token is a string which is generated by the server and it is sent to the client and the client can use that token to authenticate itself in future requests so that the server can identify the client and provide the personalized experience to the client so token is used for authentication and authorization in our application we will generate the token for the user when he register or login to our application and we will send that token to the client and the client can use that token to authenticate itself in future requests so that the server can identify the client and provide the personalized experience to the client
    //generate the token for the user why we need token ? token is used for authentication and authorization we can use token to authenticate the user and authorize the user to access the protected routes so we need to generate the token for the user when he register or login to our application so that we can use that token to authenticate and authorize the user in future requests
    const token = jwt.sign({id : user._id,username : user.username },process.env.JWT_SECRET,
        {expiresIn : "1d"}
    )

    //what is a cookie ? cookie is a small piece of data that is stored in the user's browser and it is sent to the server with every request so that the server can identify the user and provide the personalized experience to the user so we can use cookie to store the token in the user's browser so that we can use that token to authenticate and authorize the user in future requests
    res.cookie("token",token)

    res.status(201).json({
        message : "user registered successfully",
        user : {
            id : user._id,
            username : user.username,
            email : user.email
        }
    })
}


async function loginUserController(req,res){
    const {email,password} = req.body

    if(!email || !password) {
        return res.status(400).json({
            message : "all fields are required"
        })
    }

    //find the user with the given email
    const user = await userModel.findOne({email})

    if(!user) {
        return res.status(400).json({
            message : "invalid email or password"
        })
    }

    //check the password is correct or not why we need to check the password ? because we need to authenticate the user and for that we need to check the password is correct or not so we can use bcrypt.compare() method to compare the password with the hash stored in db if the password is correct then it will return true otherwise it will return false so we can use that to authenticate the user
    const isMatch = await bcrypt.compare(password,user.password)

    if(!isMatch) {
        return res.status(400).json({
            message : "invalid email or password"
        })
    }

    //generate the token
    const token = jwt.sign({id : user._id,username : user.username },process.env.JWT_SECRET,
        {expiresIn : "1d"}
    )

    //set the cookie
    res.cookie("token",token)

    res.status(200).json({
        message : "user logged in successfully",
        token,
        user : {
            id : user._id,
            username : user.username,
            email : user.email
        }
    })
}

async function logoutUserController(req,res){

    const token = req.cookies.token

    if(token) {
await tokenBlacklistModel.create({token})
    }

    //clear the cookie
    res.clearCookie("token")    
    res.status(200).json({
        message : "user logged out successfully"
    })
}


async function getMeController(req,res) {

    /*
    Browser sends cookie token
        ↓
authuser middleware verifies token
        ↓
middleware adds decoded user to req.user
        ↓
getMeController uses req.user.id
        ↓
finds user in DB
*/

    const user = await userModel.findById(req.user.id)

    if(!user) {
        return res.status(404).json({
            message : "user not found"
        })
    }

    res.status(200).json({
        message : "user found",
        user : {
            id : user._id,
            username : user.username,
            email : user.email
        }
    })
    
}


